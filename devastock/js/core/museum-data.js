// تطبيع الحروف العربية لمطابقة حروف اللعبة الـ28
const NORM = {
  'أ':'ا','إ':'ا','آ':'ا','ٱ':'ا',
  'ة':'ه',
  'ى':'ي',
};
const VALID = new Set('ابتثجحخدذرزسشصضطظعغفقكلمنهوي');

// إرجاع تكلفة الكلمة { 'ا':2, 'س':1, 'د':1 }
export function getLetterCost(word) {
  const cost = {};
  for (const char of word) {
    const c = NORM[char] ?? char;
    if (VALID.has(c)) cost[c] = (cost[c] || 0) + 1;
  }
  return cost;
}

// هل يملك اللاعب الحروف الكافية؟
export function canAfford(word, stock) {
  const cost = getLetterCost(word);
  return Object.entries(cost).every(([c, n]) => (stock[c] || 0) >= n);
}
